"""
从网络文献补充术语到 terminology.json
"""
import json

with open('rag-knowledge-base/terminology.json', 'r', encoding='utf-8') as f:
    asset = json.load(f)

existing_terms = {e['term'] for e in asset['entries']}
existing_pinyin = {e['pinyin'] for e in asset['entries']}

new_terms = []

def add(term, pinyin, category, explanation, source=''):
    if term in existing_terms or pinyin in existing_pinyin:
        return
    new_terms.append({
        'entry_id': f'term_{pinyin}',
        'entry_type': 'terminology',
        'term': term,
        'pinyin': pinyin,
        'category': category,
        'explanation': explanation,
        'source': source
    })
    existing_terms.add(term)
    existing_pinyin.add(pinyin)

